    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        String PId =txtsearch.getText();
        try{

            pst= con.prepareStatement("select * from product where id='"+PId+"' ");
            ResultSet Rs = pst.executeQuery();

            if(Rs.next()){
                txtsearch.setEditable(true);
                id2.setText(Rs.getString(1));
                txtpname.setText(Rs.getString(2));
                txtdes.setText(Rs.getString(3));
                txtbarcode.setText(Rs.getString(4));
                txtcprice.setText(Rs.getString(5));
                txtqty.setText(Rs.getString(6));
                txtrprice.setText(Rs.getString(7));
                txtlevel.setText(Rs.getString(8));

                txtsearch.setText("");
                jButton2.setEnabled(false);
                jButton4.setEnabled(true);
                jButton1.setEnabled(true);

            }
            else{
                JOptionPane.showMessageDialog(null, "That Product ID  does not exist");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);

        }

    }//GEN-LAST:event_searchbtnActionPerformed

