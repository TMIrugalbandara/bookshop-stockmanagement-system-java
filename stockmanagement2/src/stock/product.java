        if(!(Pattern.matches("[0-9]*$", txtbarcode.getText()))){
            JOptionPane.showMessageDialog(null, "Please enter a valid phone number", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(!(Pattern.matches("[0-9][0-9._]*$", txtcprice.getText()))){
            JOptionPane.showMessageDialog(null, "Please enter a valid Cost Price", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(!(Pattern.matches("[0-9][0-9._]*$", txtcprice.getText()))){
            JOptionPane.showMessageDialog(null, "Please enter a valid Retail Price", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(!(Pattern.matches("[0-9][0-9._]*$", txtqty.getText()))){
            JOptionPane.showMessageDialog(null, "Please enter a valid Qty", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(!(Pattern.matches("[0-9][0-9._]*$", txtlevel.getText()))){
            JOptionPane.showMessageDialog(null, "Please enter a valid ReOderLevel", "Error", JOptionPane.ERROR_MESSAGE);}

        try {
            String  id=id2.getText();
            String  pname=txtpname.getText();
            String des=txtdes.getText();
            String barcode=txtbarcode.getText();
            String costprice=txtcprice.getText();
            String retailprice=txtqty.getText();
            String qty=txtrprice.getText();
            String arlevel=txtlevel.getText();

            pst=con.prepareStatement("update product set pname=?,description=?,barcode=?,cprice=?,rprice=?,qty=?,rlevel=? where id=?");
            pst.setString(1, pname);
            pst.setString(2, des);
            pst.setString(3, barcode);
            pst.setString(4, costprice);
            pst.setString(5, retailprice);
            pst.setString(6, qty);
            pst.setString(7, arlevel);
            pst.setString(8, id);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Product Updated");

            txtpname.setText("");
            txtdes.setText("");
            txtbarcode.setText("");
            txtcprice.setText("");
            txtqty.setText("");
            txtrprice.setText("");
            txtlevel.setText("");
            txtpname.requestFocus();
            load();
            autoID();

            jButton4.setEnabled(true);
            jButton4.setEnabled(true);

        } catch (SQLException ex) {
            Logger.getLogger(vendor.class.getName()).log(Level.SEVERE, null, ex);
        }

